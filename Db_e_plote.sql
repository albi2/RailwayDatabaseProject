DELETE FROM Udhetim 
WHERE ID IN (
    SELECT ID 
    FROM (
        SELECT * 
        FROM (SELECT u2.ID ID, 
           EXTRACT(year FROM u1.Koha_E_Nisjes) year_1, 
           EXTRACT(year FROM u2.Koha_E_Nisjes)  year_2,
           EXTRACT(month FROM u1.Koha_E_Nisjes) month_1, 
           EXTRACT(month FROM u2.Koha_E_Nisjes) month_2,
           EXTRACT(day FROM u1.Koha_E_Nisjes) day_1, 
           EXTRACT(day FROM u2.Koha_E_Nisjes) day_2,
           EXTRACT(hour FROM u1.Koha_E_Mberritjes) time_1,
           EXTRACT(hour FROM u2.KOha_E_Nisjes) time_2
        FROM Udhetim u1 INNER JOIN Udhetim u2 ON (u1.ID <> u2.ID AND u1.Stacion_Nisje_ID = u2.Stacion_Nisje_ID AND u1.Stacion_Destinacion_ID = u2.Stacion_Destinacion_ID))
        WHERE year_1 = year_2 AND month_1 = month_2 AND day_1 = day_2 AND time_2 < time_1
    )
);
